altura = float(input("Insra em metros o tamanho da altura da parede: "))
largura = float(input("Insra em metros o tamanho da largura da parede: "))

print(f"Para uma parede de {largura} metros de largura e {altura} metros me altura, a área é: {(altura*largura)} m².")
print(f"Isso resulta em um gasto de {(largura*altura)/2:.2f} litros de tinta.")